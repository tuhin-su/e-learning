SELECT 
    a.id AS attend_id,
    a.user_id,
    u.name AS user_name,
    a.attendance_date
FROM 
    attends a
JOIN 
    user_info u ON a.user_id = u.user_id
JOIN 
    student s ON u.user_id = s.id
WHERE 
    s.course = 'BCA'
    AND s.semester = 1
    AND DATE(a.attendance_date) = CURDATE();
